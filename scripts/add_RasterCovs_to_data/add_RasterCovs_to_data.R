####  Script body ####
output<- (function(){
  
  data_points<- sf::st_read(input$spatial_points) %>% dplyr::mutate(id_row=seq(nrow(.)))
  
  
  layers_covs<- list.files(input$folder_vars, "\\.tif$", recursive = TRUE, full.names = TRUE)
  
  stack_vars<- terra::rast(layers_covs) %>% setNames(tools::file_path_sans_ext(basename(layers_covs)))
  stack_vars[["ID_grid"]]<- stack_vars[[1]] %>% terra::rast()
  stack_vars[["ID_grid"]]<- seq(ncell(stack_vars[["ID_grid"]]))
  
  
  
  id_coord<- sf::st_coordinates(data_points) %>% as.data.frame() %>% 
    dplyr::mutate(id_row=seq(nrow(.)), id_coord= as.numeric(factor(paste0(.$X, .$Y))) ) 
    
  distinct_id_coord <- id_coord %>% dplyr::filter(!duplicated(id_coord)) %>% 
    sf::st_as_sf(coords = c("X", "Y"), crs = crs(data_points)) %>% dplyr::select(id_coord)
  
  
  # Generar matriz pixeles por puntos y covariables
  covars_ID<-  raster::extract(stack_vars, distinct_id_coord) %>% {.[,2:length(.)]} %>% 
    dplyr::mutate(id_coord= distinct_id_coord$id_coord)

  join_covars<- id_coord %>% list(covars_ID) %>% 
    plyr::join_all() %>% list(as.data.frame(data_points)) %>% plyr::join_all() %>% dplyr::select(-c("id_row", "id_coord"))
  
  # Salida final
  spatial_covars<- sf::st_as_sf(join_covars)
  data_covars<- sf::st_drop_geometry(spatial_covars)
  
  data_covars_path<- file.path(outputFolder, "vars_dataset.csv") # Define the file path for the 'val_wkt_path' output
  write.csv(data_covars, data_covars_path, row.names = F ) # Write the 'val_wkt_path' output
  
  
  
  spatial_covars_path<- file.path(outputFolder, "spatial_covars.GeoJSON") # Define the file path for the 'val_wkt_path' output
  sf::st_write(spatial_covars, spatial_covars_path, delete_dsn= T, driver = "GeoJSON")
  
  
  
  output<- list(vars_dataset= data_covars_path, spatial_dataset=spatial_covars_path)
  
})()




#### Outputing result to JSON ####

# Write the output list to the 'output.json' file in JSON format
setwd(outputFolder)
jsonlite::write_json(output, "output.json", auto_unbox = TRUE, pretty = TRUE)
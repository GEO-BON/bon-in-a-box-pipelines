input<- list(species_data = "D:/Repositorios/biab-2.0/scripts/00_redlistindex/input/data_sp.csv", species_list= "scientific_name", parellel= T)

#### Species list by country ####
#Load IUCN token----
token <- "f33e69dfa9b06a6495aca3b049606f6e08ceb37083ff88a9c7c9dfbcd56a9121"

species_data<- read.csv(input$species_data)
species_list<- species_data[, input$species_list][1:5]


cl<- parallel::detectCores()/2
plan(sequential, gc = TRUE); 

plan(multisession, workers=cl, gc = TRUE)


with_progress({
  p <- progressr::progressor(along= seq_along(species_list))
  list_IUCN_history <-  future.apply::future_lapply(species_list, function(x)   { p()
    tryCatch({
    rredlist::rl_history(x, key= token)$result %>% dplyr::mutate(sp= x)
    }, error= function(e) {data.frame(sp=x)})
  }, future.chunk.size=1 )
})

IUCN_history_data<- plyr::rbind.fill(list_IUCN_history) %>%
  dplyr::group_by(sp) %>% dplyr::mutate(n_years= dplyr::n_distinct(year)) %>% 
  dplyr::relocate( c("sp", "n_years"),1) %>% 
  dplyr::mutate(sp= factor(sp, levels= unique(.$sp))) %>%  dplyr::arrange(sp, year) %>% 
  dplyr::mutate(code = ifelse(code == "LR/lc", "LC", code))

IUCN_history_matrix<- IUCN_history_data %>% 
  reshape2::dcast( sp ~ year, value.var = "code") %>% tibble::column_to_rownames("sp")


rli(IUCN_history_matrix, boot = F, dd = T)



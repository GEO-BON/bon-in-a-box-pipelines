output<- tryCatch({



# Correr codigo
table_data<- read.csv(input$table_data,
                      header = T, check.names = F)

table_reclassify<- read.csv(input$table_reclassify,
                            header = T, check.names = F)

  


layers_stack<- dplyr::select(table_data, c("period", "file")) %>% dplyr::distinct()

setwd(input$dir_stack)
stack_layers<- terra::rast(layers_stack$file) %>% setNames(layers_stack$period)

data_stack<- as.data.frame(stack_layers)



category_stack<- lapply(unique(table_data$period), function(i) {
  
  dat<- table_data[table_data$period %in% i,]
  
  setwd(input$dir_stack)
  layer<- terra::rast(unique(dat$file))
  layer_data<- as.data.frame(layer) %>% setNames("value")
  metadata_layer<- dplyr::select(dat, c("value", "classes"))
  
  classes_data<- list(layer_data, metadata_layer) %>% plyr::join_all() %>% dplyr::select("classes") %>% 
    setNames(i)
  
  list(layer= layer, reclass= classes_data)

}) %>% setNames(unique(table_data$period))


periods<- list()
  
for (i in seq_along(category_stack)) {
  if (i == length(category_stack)) {
    break 
  } else {
    periods[[i]] <- c(names(category_stack)[i], names(category_stack)[i + 1])
  }
}


compare_data<- lapply(periods, function(y) {
  
  dat_compare<- data.frame(category_stack[[y[1]]]$reclass, category_stack[[y[2]]]$reclass) %>% 
    setNames(names(table_reclassify)[1:2]) %>% list(table_reclassify) %>% plyr::join_all()
  
  data_compare<- dplyr::select(dat_compare, "reclass") %>% dplyr::group_by(reclass) %>% 
    dplyr::summarize(area= (dplyr::n()) * 1000 ) %>% dplyr::mutate(period1= y[1], period2= y[2]) %>% 
    dplyr::relocate(c("reclass", "area"), .after = ncol(.))
  
}) %>% plyr::rbind.fill()


dir_compare_data<- file.path(outputFolder, "compare_data.csv")

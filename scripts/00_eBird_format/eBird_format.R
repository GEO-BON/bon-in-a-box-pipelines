# filter prior to creating occupancy model data
ebird_filtered <- dplyr::filter(ebd_occ, 
                                number_observers <= input$max_obs,
                                number_observers >= input$min_obs
                                )


# Data formatting
occ <- auk::filter_repeat_visits(ebird_filtered, 
                            min_obs = input$min_obs, max_obs = input$max_obs,
                            annual_closure = input$annual_closure,
                            n_days = input$n_days,
                            date_var = "observation_date",
                            site_vars = input$site_vars)



# format for unmarked

site_covs_input<- lapply(input$site_covs, function(y) { text_ext<- tools::file_ext(y)

  if(file.exists(y)){y}else{NULL}
} else {
  test_folder<- dir.exists(y)
  if(test_folder){ list.files(y, full.names = T, recursive = F, pattern = "\\.tif") %>%  {.[!grepl("\\.tfw$|~$", .)]} } else { y }
}
